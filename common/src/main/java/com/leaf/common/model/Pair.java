package com.leaf.common.model;

public class Pair<A, B> {

    private A a;

    private B b;

    public Pair() {
    }

    public A getA() {
        return a;
    }

    public void setA(A a) {
        this.a = a;
    }

    public B getB() {
        return b;
    }

    public void setB(B b) {
        this.b = b;
    }
}
